"use client";

import React from "react";
import ReactMarkdown from "react-markdown";
import { SummariseResponse } from "@/lib/api";
import { Skeleton } from "@/components/ui/skeleton";
import { Card, CardContent, CardHeader, CardTitle, CardDescription } from "@/components/ui/card";
import { Badge } from "@/components/ui/badge";
import { Separator } from "@/components/ui/separator";

interface ResultsProps {
  data: SummariseResponse | null;
  isLoading: boolean;
}

export function Results({ data, isLoading }: ResultsProps) {
  if (!data && !isLoading) return null;

  return (
    <div className="grid grid-cols-1 md:grid-cols-2 gap-8 mt-12 w-full max-w-6xl mx-auto">
      {/* Left Column: Commits Breakdown */}
      <Card className="border-border bg-card/60 backdrop-blur-sm h-full flex flex-col">
        <CardHeader className="pb-4">
          <CardTitle className="text-xl">Commit Activity</CardTitle>
          <CardDescription>
            {isLoading ? (
              <Skeleton className="h-4 w-[200px]" />
            ) : data ? (
              <span className="flex items-center gap-2">
                <span>{data.repos.length} repo(s) over {data.days} days</span>
                <Badge variant="secondary" className="font-mono">{data.repos.join(", ")}</Badge>
              </span>
            ) : null}
          </CardDescription>
        </CardHeader>
        <CardContent className="flex-1 overflow-hidden relative">
          {isLoading ? (
            <div className="space-y-4">
              <Skeleton className="h-8 w-full" />
              <Skeleton className="h-8 w-5/6" />
              <Skeleton className="h-8 w-full" />
              <Skeleton className="h-8 w-4/6" />
            </div>
          ) : data ? (
            <pre className="p-4 bg-muted/50 rounded-md font-mono text-sm max-h-[600px] overflow-y-auto whitespace-pre-wrap border border-sidebar-border hidden-scrollbar custom-scrollbar">
              {data.display}
            </pre>
          ) : null}
        </CardContent>
      </Card>

      {/* Right Column: AI Summary */}
      <Card className="border-border bg-card/60 backdrop-blur-sm h-full flex flex-col">
        <CardHeader className="pb-4">
          <CardTitle className="text-xl text-primary">Standup Summary</CardTitle>
          <CardDescription>
            {isLoading ? (
              <Skeleton className="h-4 w-[120px]" />
            ) : data ? (
              "Generated by AI"
            ) : null}
          </CardDescription>
        </CardHeader>
        <CardContent className="flex-1 relative overflow-auto">
          {isLoading ? (
            <div className="space-y-4">
              <Skeleton className="h-12 w-1/3" />
              <Skeleton className="h-4 w-full" />
              <Skeleton className="h-4 w-full" />
              <Skeleton className="h-4 w-5/6" />
              <Separator />
              <Skeleton className="h-8 w-1/4" />
              <Skeleton className="h-4 w-full" />
              <Skeleton className="h-4 w-2/3" />
            </div>
          ) : data ? (
            <div className="prose prose-invert prose-p:text-muted-foreground prose-headings:text-foreground prose-a:text-primary max-w-none prose-sm sm:prose-base max-h-[600px] overflow-y-auto pr-2 custom-scrollbar">
              <ReactMarkdown>{data.summary}</ReactMarkdown>
            </div>
          ) : null}
        </CardContent>
      </Card>
      
      {/* Required styles for syntax rendering */}
      <style dangerouslySetInnerHTML={{__html: `
        .custom-scrollbar::-webkit-scrollbar {
          width: 8px;
        }
        .custom-scrollbar::-webkit-scrollbar-track {
          background: transparent;
        }
        .custom-scrollbar::-webkit-scrollbar-thumb {
          background-color: var(--border);
          border-radius: 4px; border: 2px solid var(--card);
        }
      `}} />
    </div>
  );
}
